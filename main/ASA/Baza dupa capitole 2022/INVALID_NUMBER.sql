
SELECT 
D.CUIIO,
D.RIND,
D.COL31,
D.COL8, 
D.COL9,
D.COL10,
D.COL33,   
D.COL37    

   
 FROM  CIS2.VW_DATA_ALL D    
    
                    INNER JOIN CIS2.RENIM R ON R.CUIIO = D.CUIIO AND R.CUIIO_VERS = D.CUIIO_VERS  
                
WHERE
  D.FORM IN (64)             AND 
     
  D.PERIOADA =:pPERIOADA AND
  D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%'   AND
  D.CAPITOL IN (1128) 
  
 

GROUP BY 

D.CUIIO,
D.RIND,
D.COL31,
D.COL8, 
D.COL9,
D.COL10,
D.COL33,   
D.COL37,    
D.COL8, 
D.COL9, 
D.COL10

HAVING 
D.COL37 IS NOT NULL
ORDER BY
D.COL37 DESC