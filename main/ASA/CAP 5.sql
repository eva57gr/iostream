SELECT 
D.ANUL,
D.CUIIO,
R.IDNO,
R.DENUMIRE,
R.CUATM,
R.CFP,
R.CFOJ,
R.CAEM2,
D.RIND,
MAX(D.COL33) CUATM_SEC,
MAX(D.COL37) CAEM_SEC,
SUM(D.COL8) AS COL1,
SUM(D.COL9) AS COL2,
SUM(D.COL10) AS COL3



FROM

    CIS2.VW_DATA_ALL_COEF D




                    INNER JOIN CIS2.RENIM R ON R.CUIIO = D.CUIIO AND R.CUIIO_VERS = D.CUIIO_VERS
WHERE
  D.FORM IN (64)             AND

  D.PERIOADA =:pPERIOADA AND
  D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%'   AND
  D.CAPITOL IN (1128)
--  AND D.CUIIO = 34275
  
  
  GROUP BY 
  D.ANUL,
D.CUIIO,
R.IDNO,
R.DENUMIRE,
R.CUATM,
R.CFP,
R.CFOJ,
R.CAEM2,
D.RIND


ORDER BY
D.CUIIO,
D.RIND
