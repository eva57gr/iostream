INSERT INTO CIS2.TABLE_OUT 
(
  PERIOADA,
  FORM,
  FORM_VERS,
  ID_MDTABLE,
  COD_CUATM,
  NR_SECTIE,
  NUME_SECTIE,
  NR_SECTIE1,
  NUME_SECTIE1,
  NR_SECTIE2,
  NUME_SECTIE2,
  NR_ROW,
  ORDINE,
  DECIMAL_POS,
  NUME_ROW,  
 COL1, COL2, COL3,COL4,COL5,COL6,COL7,COL8,COL9,COL10,COL11, COL12, COL13, COL14, COL15, COL16, COL17, COL18, COL19,
    COL20, COL21, COL22,COL23,COL24,COL25,COL26,COL27,COL28
    )

SELECT 
:pPERIOADA AS PERIOADA,
:pFORM AS FORM,
:pFORM_VERS AS FORM_VERS,
:pID_MDTABLE AS ID_MDTABLE,
:pCOD_CUATM AS COD_CUATM,    
'0' AS  NR_SECTIE,
'0' AS NUME_SECTIE,
'0' AS NR_SECTIE1,
'0' AS NUME_SECTIE1,
'0' AS NR_SECTIE2,
'0' AS NUME_SECTIE2, 
NR_ROW||'~'||ROWNUM AS NR_ROW,
ROWNUM AS ORDINE,
'0000000000000000000000000000' AS DECIMAL_POS,
NUME_ROW,
COL1, COL2, COL3,COL4,COL5,COL6,COL7,COL8,COL9,COL10,COL11, COL12, COL13, COL14, COL15, COL16, COL17, COL18, COL19,
    COL20, COL21, COL22,COL23,COL24,COL25,COL26,COL27,COL28
--    
FROM
(
 SELECT
D.CODUL AS NR_ROW,
ORDINE,
D.DENUMIRE AS NUME_ROW,
COL1, COL2, COL3,COL4,COL5,COL6,COL7,COL8,COL9,COL10,COL11, COL12, COL13, COL14, COL15, COL16, COL17, COL18, COL19,
    COL20, COL21, COL22,COL23,COL24,COL25,COL26,COL27,COL28
    
FROM(
SELECT 
   CC.FULL_CODE,
   CC.DENUMIRE,
   CC.CODUL,
   CC.FULL_CODE AS ORDINE,
   COUNT(DISTINCT DD.CUIIO ) AS COL1, 
   COUNT(DISTINCT CASE  WHEN DD.COL0 BETWEEN 0 AND 3   THEN DD.CUIIO  END) AS COL2, 
   COUNT(DISTINCT CASE  WHEN DD.COL0 BETWEEN 3 AND 6   THEN DD.CUIIO  END) AS COL3, 
   COUNT(DISTINCT CASE  WHEN DD.COL0 BETWEEN 6 AND 9   THEN DD.CUIIO  END) AS COL4, 
   COUNT(DISTINCT CASE  WHEN DD.COL0 BETWEEN 9 AND 12  THEN DD.CUIIO  END) AS COL5,
  
    SUM(CASE WHEN DR.RIND IN ('300') THEN  DR.COL1 END) AS COL6,
   SUM(CASE WHEN DR.RIND IN ('300') THEN  DR.COL2 END) AS COL7,
   SUM(CASE WHEN DR.RIND IN ('310') THEN  DR.COL1 END) AS COL8,
   SUM(CASE WHEN DR.RIND IN ('310') THEN  DR.COL2 END) AS COL9,
   SUM(CASE WHEN DR.RIND IN ('320') THEN  DR.COL1 END) AS COL10,
   SUM(CASE WHEN DR.RIND IN ('320') THEN  DR.COL2 END) AS COL11,
   SUM(CASE WHEN DR.RIND IN ('330') THEN  DR.COL1 END) AS COL12,
   SUM(CASE WHEN DR.RIND IN ('330') THEN  DR.COL2 END) AS COL13,
   SUM(CASE WHEN DR.RIND IN ('340') THEN  DR.COL1 END) AS COL14,
   SUM(CASE WHEN DR.RIND IN ('340') THEN  DR.COL2 END) AS COL15,
   SUM(CASE WHEN DR.RIND IN ('350') THEN  DR.COL1 END) AS COL16,
   SUM(CASE WHEN DR.RIND IN ('350') THEN  DR.COL2 END) AS COL17,
   SUM(CASE WHEN DR.RIND IN ('360') THEN  DR.COL1 END) AS COL18,
   SUM(CASE WHEN DR.RIND IN ('360') THEN  DR.COL2 END) AS COL19,
   
   SUM(CASE WHEN DR.RIND IN ('400') THEN  DR.COL3 END) AS COL20,
   SUM(CASE WHEN DR.RIND IN ('400') THEN  DR.COL4 END) AS COL21,
   SUM(CASE WHEN DR.RIND IN ('400') THEN  DR.COL5 END) AS COL22,
   SUM(CASE WHEN DR.RIND IN ('500') THEN  DR.COL8 END) AS COL23,
   SUM(CASE WHEN DR.RIND IN ('500') THEN  DR.COL9 END) AS COL24,
   SUM(CASE WHEN DR.RIND IN ('500') THEN  DR.COL10 END) AS COL25,
   SUM(CASE WHEN DR.RIND IN ('510') THEN  DR.COL8 END) AS COL26,
   SUM(CASE WHEN DR.RIND IN ('510') THEN  DR.COL9 END) AS COL27,
   SUM(CASE WHEN DR.RIND IN ('510') THEN  DR.COL10 END) AS COL28
                                                                                     
                
    FROM
    
    
    CIS2.VW_DATA_ALL DR INNER JOIN
    
    (
-------------------------------------------------------------------------------     
    SELECT  
    DISTINCT 
    D.CUIIO,
    D.CUIIO_VERS,
    MAX(CASE WHEN  D.ID_MD IN (61832) THEN  D.COL31 ELSE NULL END)  AS CAEM_ACT,
    
    CASE WHEN MAX(CASE WHEN  D.ID_MD IN (61832) THEN  D.COL31 ELSE NULL END) IS NULL 
 THEN    MAX(SUBSTR(R.CAEM2,2,4))  ELSE MAX(CASE WHEN  D.ID_MD IN (61832) THEN  D.COL31 ELSE NULL END)  END 
 
 AS CAEM2_ACTUALIZAT,
    NVAL(SUM(CASE WHEN  D.ID_MD IN (59334,59336,59337,59338,59339,59340,59341,59342,59343,59344,59345,59346) THEN  D.COL1 ELSE 0 END))  AS COL0 ,  
    NVAL(SUM(CASE WHEN  D.ID_MD IN (69986) THEN  D.COL4 ELSE 0 END))  AS COL4,
    
   NVAL(SUM(CASE WHEN  D.ID_MD IN (62382,62413) THEN  D.COL1 ELSE 0 END))  AS CAP_SR
    
    
    --61832 
FROM   
    CIS2.DATA_ALL D   INNER JOIN CIS2.RENIM R ON R.CUIIO = D.CUIIO  AND R.CUIIO_VERS = D.CUIIO_VERS   
WHERE
  D.FORM IN (64)             AND 
  D.FORM_VERS = :PFORM_VERS  AND      
  D.PERIOADA =:pPERIOADA --AND
 -- D.ID_MD IN (59334,59336,59337,59338,59339,59340,59341,59342,59343,59344,59345,59346,69986)
 --AND D.CUIIO = 34275
 
 GROUP BY 
 D.CUIIO,
 D.CUIIO_VERS
 
 
 HAVING 
 NVAL(SUM(CASE WHEN  D.ID_MD IN (62382,62413) THEN  D.COL1 ELSE 0 END)) > 0
 
 AND 
  NVAL(SUM(CASE WHEN  D.ID_MD IN (62451) THEN  D.COL1 ELSE 0 END)) > 0
 
 
 
) DD ON DR.CUIIO=DD.CUIIO AND DR.CUIIO_VERS=DD.CUIIO_VERS
-------------------------------------------------------------------------------    

 INNER JOIN (
 SELECT
CODUL, 
DENUMIRE, 
GRUPA, 
ORDINE, 
FULL_CODE, 
NUM_CODE, 
PRIM                
                FROM  CIS2.VW_CL_CAEM2
                
               
                
                WHERE 
                CODUL LIKE 'C%'
                OR CODUL LIKE 'B%'
                OR CODUL LIKE 'D%'
                OR CODUL LIKE 'E%'
                OR CODUL LIKE 'F%'
                OR CODUL LIKE 'G%'
                OR CODUL LIKE 'H%'
                OR CODUL LIKE 'I%'
                OR CODUL LIKE 'J%'
                OR CODUL LIKE 'L%'
                OR CODUL LIKE 'M%'
                OR CODUL LIKE 'N%'
                OR CODUL LIKE 'S%'
             
                
                
                ) C ON      SUBSTR( C.CODUL,2,4) = DD.CAEM2_ACTUALIZAT              
               INNER JOIN CIS2.VW_CL_CAEM2 CC ON (C.FULL_CODE LIKE '%'||CC.CODUL||';%') 
         
 WHERE 
  (DR.FORM=:pFORM) AND
  (DR.FORM_VERS=:pFORM_VERS) AND
  (:pID_MDTABLE=:pID_MDTABLE) AND
--  (DR.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%') AND   
  DR.PERIOADA IN (:pPERIOADA) 
 -- AND  C.CODUL LIKE '%0'
 -- AND  DR.CUIIO=40856685
-- AND 
-- CC.CODUL LIKE 'C%'
 
   GROUP BY
    CC.FULL_CODE,
    CC.DENUMIRE,
    CC.CODUL
   ORDER BY 
     
     CC.FULL_CODE
     ) D
     
      WHERE
         
         D.COL1+D.COL2+ D.COL3+D.COL4+D.COL5>0 
         )
    