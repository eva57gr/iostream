    CIS2.VW_DATA_ALL D 
                    INNER JOIN CIS2.VW_CL_CUATM C ON C.CODUL = D.CUATM
    
                      
WHERE
  D.FORM IN (64) AND 
  D.PERIOADA = 2012 
  -- AND D.CUIIO = 135094
  
  
  GROUP BY 
  D.CUIIO,
  D.CUATM,
  C.FULL_CODE,
  D.CAEM2
 
  
  ORDER BY 
  C.FULL_CODE

  
  