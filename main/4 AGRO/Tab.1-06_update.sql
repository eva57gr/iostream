SELECT
  :pPERIOADA AS PERIOADA,
  :pFORM AS FORM,
  :pFORM_VERS AS FORM_VERS,
  :pID_MDTABLE AS ID_MDTABLE,
  :pCOD_CUATM AS COD_CUATM,
  '0' AS NR_SECTIE,
  '0' AS NUME_SECTIE,
  '0' AS NR_SECTIE1,
  '0' AS NUME_SECTIE1,
  '0' AS NR_SECTIE2,
  '0' AS NUME_SECTIE2,
   NR_ROW||'~'||NUME_ROW,
   ORDINE,
  '010000' AS DECIMAL_POS,
   NUME_ROW,
   COL1,
   COL2,
   COL3,
   COL4,
   COL5,
   COL6
  
FROM
(
SELECT 
 SUM(NR_ROW) AS NR_ROW,  
  0  AS ORDINE,
 'TOTAL' AS NUME_ROW,
  SUM(COL1) AS COL1,
 (SUM(COL1)/SUM(NR_ROW)*100) AS COL2,
  SUM(COL3) AS COL3,
  SUM(COL4) AS COL4,
  SUM(COL5) AS COL5,
  SUM(COL6) AS COL6
  
FROM

(
SELECT
  ROUND(COL1,0) AS NR_ROW,
  CUIIO AS ORDINE,
  DENUMIRE||' - '||CUIIO AS NUME_ROW,
  ROUND(COL2,0) AS COL1,
  ROUND((COL2/NOZERO(COL1))*100,0) AS COL2,
  ROUND(COL4,0) AS COL3,
  ROUND(COL5,0) AS COL4,
  ROUND(COL6,0) AS COL5,
  ROUND(COL7,0) AS COL6
  
FROM
(
SELECT 
  R.CUIIO,
  R.DENUMIRE,
  SUM(CASE WHEN D.RIND IN ('015')  AND D.CFOJ IN (500,530,540,541,550,590,690,880,890,684,685) AND D.PERIOADA IN (:pPERIOADA-1)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016')  AND D.CFOJ IN (500,530,540,541,550,590,690,880,890,684,685) AND D.PERIOADA IN (:pPERIOADA-1)  THEN D.COL1 ELSE 0 END) AS COL1,
  
  
  SUM(CASE WHEN D.RIND IN ('015') AND D.CFOJ IN (500,530,540,541,550,590,690,880,890,684,685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016') AND D.CFOJ IN (500,530,540,541,550,590,690,880,890,684,685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END) AS COL2,
  
  SUM(CASE WHEN D.RIND IN ('015')  AND D.CFOJ IN (500,530,540,541,550,590,690,880,890) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016')  AND D.CFOJ IN (500,530,540,541,550,590,690,880,890) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END) AS COL4,
  
  
  SUM(CASE WHEN D.RIND IN ('015')  AND D.CFOJ IN (684,685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016')  AND D.CFOJ IN (684,685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END) AS COL5,
  
  
  SUM(CASE WHEN D.RIND IN ('015')  AND D.CFOJ IN (685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016')  AND D.CFOJ IN (685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END) AS COL6,
  
  
  SUM(CASE WHEN D.RIND IN ('015') AND D.CFOJ IN (684) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016') AND D.CFOJ IN (684) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END) AS COL7
FROM 
  VW_DATA_ALL D
  INNER JOIN CIS2.RENIM R ON R.CUIIO = D.CUIIO AND R.CUIIO_VERS = D.CUIIO_VERS
WHERE
  D.FORM IN (43)                 AND 
  D.FORM_VERS = :pFORM_VERS     AND    
  D.CAPITOL IN (397)            AND 
  D.PERIOADA IN (:pPERIOADA,:pPERIOADA-1) AND 
  D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%' AND
  D.RIND IN ('015','016')
GROUP BY 
  R.CUIIO,
  R.DENUMIRE
)

)

UNION


SELECT
  ROUND(COL1,0) AS NR_ROW,
  CUIIO AS ORDINE,
  DENUMIRE||' - '||CUIIO AS NUME_ROW,
  ROUND(COL2,0) AS COL1,
 (COL2/NOZERO(COL1))*100 AS COL2,
  ROUND(COL4,0) AS COL3,
  ROUND(COL5,0) AS COL4,
  ROUND(COL6,0) AS COL5,
  ROUND(COL7,0) AS COL6
  
FROM
(
SELECT 
  R.CUIIO,
  R.DENUMIRE,
  SUM(CASE WHEN D.RIND IN ('015')  AND D.CFOJ IN (500,530,540,541,550,590,690,880,890,684,685) AND D.PERIOADA IN (:pPERIOADA-1)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016')  AND D.CFOJ IN (500,530,540,541,550,590,690,880,890,684,685) AND D.PERIOADA IN (:pPERIOADA-1)  THEN D.COL1 ELSE 0 END) AS COL1,
  
  
  SUM(CASE WHEN D.RIND IN ('015') AND D.CFOJ IN (500,530,540,541,550,590,690,880,890,684,685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016') AND D.CFOJ IN (500,530,540,541,550,590,690,880,890,684,685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END) AS COL2,
  
  SUM(CASE WHEN D.RIND IN ('015')  AND D.CFOJ IN (500,530,540,541,550,590,690,880,890) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016')  AND D.CFOJ IN (500,530,540,541,550,590,690,880,890) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END) AS COL4,
  
  
  SUM(CASE WHEN D.RIND IN ('015')  AND D.CFOJ IN (684,685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016')  AND D.CFOJ IN (684,685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END) AS COL5,
  
  
  SUM(CASE WHEN D.RIND IN ('015')  AND D.CFOJ IN (685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016')  AND D.CFOJ IN (685) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END) AS COL6,
  
  
  SUM(CASE WHEN D.RIND IN ('015') AND D.CFOJ IN (684) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END)-
  SUM(CASE WHEN D.RIND IN ('016') AND D.CFOJ IN (684) AND D.PERIOADA IN (:pPERIOADA)  THEN D.COL1 ELSE 0 END) AS COL7
FROM 
  VW_DATA_ALL D
  INNER JOIN CIS2.RENIM R ON R.CUIIO = D.CUIIO AND R.CUIIO_VERS = D.CUIIO_VERS
WHERE
  D.FORM IN (43)                 AND 
  D.FORM_VERS = :pFORM_VERS     AND    
  D.CAPITOL IN (397)            AND 
  D.PERIOADA IN (:pPERIOADA,:pPERIOADA-1) AND 
  D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%' AND
  D.RIND IN ('015','016')
GROUP BY 
  R.CUIIO,
  R.DENUMIRE
)

);