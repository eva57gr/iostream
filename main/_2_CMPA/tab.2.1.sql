SELECT 
    :pPERIOADA AS PERIOADA,
    :pFORM AS FORM,
    :pFORM_VERS AS FORM_VERS,
    :pID_MDTABLE AS ID_MDTABLE,
    :pCOD_CUATM AS COD_CUATM,
   '0' AS  NR_SECTIE,
    '0' AS NUME_SECTIE,
    '0' AS NR_SECTIE1,
    '0' AS NUME_SECTIE1,
    '0' AS NR_SECTIE2,
    '0' AS NUME_SECTIE2,
    COUNT(DISTINCT CASE WHEN MR.RIND IN ('0') AND C.FULL_CODE LIKE '%'||CC.CODUL||'%' THEN D.UNIT_CODE  ELSE NULL END)||'~'|| CC.CODUL AS NR_ROW,
    CC.ORDINE AS ORDINE,
    '001' AS DECIMAL_POS,
    CC.DENUMIRE AS NUME_ROW,
    COUNT(DISTINCT CASE WHEN MR.RIND IN ('TIP3') AND C.FULL_CODE LIKE '%'||CC.CODUL||'%' THEN D.UNIT_CODE  ELSE NULL END) AS COL1,
    COUNT(DISTINCT CASE WHEN MR.RIND IN ('TIP2') AND C.FULL_CODE LIKE '%'||CC.CODUL||'%' THEN   D.UNIT_CODE  ELSE NULL END) AS COL2,
    COUNT(DISTINCT CASE WHEN MR.RIND NOT IN ('TIP2', 'TIP3','0') AND C.FULL_CODE LIKE '%'||CC.CODUL||'%' THEN   D.UNIT_CODE  ELSE NULL END) AS COL3
FROM
  VW_DATA_ALL_GC  D
  INNER JOIN MD_RIND MR ON D.ID_MD = MR.ID_MD
  INNER JOIN VW_CL_CUATM C ON(D.CUATM = C.CODUL)
  CROSS JOIN 
 (
    SELECT
      DECODE(CS.CODUL, 
      '0000000','01','0100000','02','1111111','03','0300000','04','1400000','05',
      '3400000','06','3600000','07','4100000','08','4300000','09',
      '4500000','10','4800000','11','6200000','12','7100000','13','7400000','14',
      '7800000','15','2222222','16','1000000','17','2500000','18','3100000','19',
      '3800000','20','5300000','21','5500000','22','6000000','23','6400000','24',
      '6700000','25','8000000','26','8300000','27','8900000','28','9200000','29',
      '3333333','30','1200000','31','1700000','32','2100000','33','2700000','34',
      '2900000','35','5700000','36','8500000','37','8700000','38','9600000','39')
      AS ORDINE,
      CS.DENUMIRE AS DENUMIRE,
      CS.CODUL AS CODUL
    FROM
      VW_CL_CUATM CS
    WHERE
      CS.CODUL 
      IN(
         '0000000','0100000','1111111','0300000','1400000','3400000','3600000','4100000','4300000','4500000',
         '4800000','6200000','7100000','7400000','7800000','2222222','1000000','2500000','3100000','3800000',
         '5300000','5500000','6000000','6400000','6700000','8000000','8300000','8900000','9200000','3333333',
         '1200000','1700000','2100000','2700000','2900000','5700000','8500000','8700000','9600000'
        )
  ) CC
WHERE
  (D.PERIOADA IN (:pPERIOADA))  AND   
  (D.FORM =:pFORM) AND
  (D.FORM_VERS =:pFORM_VERS) AND 
  (:pID_MDTABLE =:pID_MDTABLE) AND
  D.FORM IN (58)  AND
  D.CAPITOL IN (419)
GROUP BY
  CC.CODUL,
  CC.DENUMIRE,
  CC.ORDINE