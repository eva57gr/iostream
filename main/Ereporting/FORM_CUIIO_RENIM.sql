SELECT 
 DISTINCT 
 D.CUIIO,
 D.CUIIO_VERS,
 MAX(D.DATA_REG) DATA_REG  
 FROM USER_EREPORTING.VW_DATA_ALL_PRIMIT D
 
 WHERE 
D.FORM = 48
AND D.PERIOADA = :pPERIOADA

GROUP BY
D.CUIIO,
 D.CUIIO_VERS
 
 
ORDER BY
DATA_REG DESC
 ;

