


SELECT 
DISTINCT D.CUIIO,
D.CUIIO_VERS,
D.CAPITOL,
D.RIND,
D.ID_MD,
SUM(D.COL1) AS COL1,
SUM(D.COL2) AS COL2,
SUM(D.COL3) AS COL3,
SUM(D.COL4) AS COL4


FROM USER_EREPORTING.VW_DATA_ALL_FOR_VALIDATE D 

WHERE 
D.FORM IN  (45)

AND D.PERIOADA = 1062
-----------------------------------------------------

AND D.CUIIO IN (

274936378,
2749363   
)


GROUP BY 
D.CUIIO,
D.CUIIO_VERS,
D.CAPITOL,
D.RIND,
D.ID_MD
ORDER BY 
D.CUIIO,
D.CUIIO_VERS,
D.CAPITOL,
D.RIND
