SELECT DISTINCT
        D.CUIIO,
        MF.FORM,
        MF.DEN_SHORT
    FROM
        CIS2.VW_DATA_ALL D
        INNER JOIN CIS2.MD_FORM MF ON MF.FORM = D.FORM AND MF.FORM_VERS = D.FORM_VERS
    WHERE
        D.ANUL = :pANUL
        AND D.CUIIO IN (
            SELECT CUIIO 

    FROM USER_BANCU.LIST_OF_REPORTS )