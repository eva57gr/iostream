SELECT DISTINCT
  'Codul CUIIO='||D.CUIIO||' exista deja in sistemul BNS'
  AS REZULTAT

FROM
  CIS.VW_DATA_ALL D

WHERE
  (D.PERIOADA=:PERIOADA) AND
  (D.CUIIO=:CUIIO ) AND
  (:CUIIO_VERS=:CUIIO_VERS     ) AND
  (D.FORM = :FORM ) AND
  (:FORM_VERS=:FORM_VERS       ) AND
  (:CAPITOL=:CAPITOL) AND
  (:CAPITOL_VERS=:CAPITOL_VERS) AND
  (:ID_MD=:ID_MD) AND
   D.FORM =1