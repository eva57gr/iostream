SELECT 

DISTINCT D.CUIIO,
D.T_XML_FORM_ID,
         MAX(D.DATA_REG) DATA_REG  

FROM USER_EREPORTING.VW_DATA_ALL_PRIMIT D


WHERE 

D.FORM IN (:pFORM)
AND D.PERIOADA IN (:pPERIOADA)


AND D.CUIIO IN (
40256577,
40104442,
40977816,
40928806,
40247228,
40253981,
41131872,
38263742

)


GROUP BY 

D.CUIIO,
D.T_XML_FORM_ID

ORDER BY 
DATA_REG DESC 