
SELECT
DISTINCT 
  D.CUIIO,
  --D.RIND,
  D.COL2 AS COL1
  
FROM 
  CIS2.VW_DATA_ALL D
  
WHERE
 D.PERIOADA IN (1052,1053,1054,1055) AND
  
 
 
  D.FORM IN (26)AND   
  D.CAPITOL IN (367) AND
  --D.RIND IN ('11','12','27','29','30')
  D.RIND IN ('11')

GROUP BY
   D.CUIIO,
   D.COL2
   

   HAVING 
   
   D.COL2 >= 1200 AND  D.COL2 <= 2200
--   
--   SUM(D.COL2) BETWEEN  1200 AND 2200
--   
--      
   ORDER BY
   D.CUIIO
   
   