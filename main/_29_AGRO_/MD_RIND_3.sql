
SELECT 
D.RIND,
D.RIND_VERS,
D.ORDINE
FROM CIS2.MD_RIND D

INNER JOIN 
(
SELECT 
RIND,
MAX(RIND_VERS) RIND_VERS,
ORDINE
FROM CIS2.MD_RIND
   WHERE
    FORM = 45
    AND 
    capitol IN (399)
    GROUP BY
    RIND, 
    ORDINE
    ) DD ON D.RIND=DD.RIND AND D.RIND_VERS=DD.RIND_VERS



WHERE 
D.STATUT = '1'

ORDER BY 
D.ORDINE