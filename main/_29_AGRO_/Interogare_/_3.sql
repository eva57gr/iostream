


CREATE OR REPLACE FORCE VIEW VW_29_AGRO_1063
(
  CUIIO,
  CUIIO_VERS,
  DENUMIRE,
  CUATM,
  DENUMIRE_CUATM,
  CAEM2,
  FULL_CODE
 
 )
AS 



SELECT
  D.CUIIO,
  D.CUIIO_VERS,
  R.DENUMIRE,
  C.CODUL CUATM,
  C.DENUMIRE DENUMIRE_CUATM,
  R.CAEM2,
  C.FULL_CODE
  
FROM 
  CIS2.VW_DATA_ALL D INNER JOIN CIS2.VW_CL_CUATM C ON C.CODUL = D.CUATM
                INNER JOIN CIS2.RENIM  R  ON R.CUIIO = D.CUIIO AND R.CUIIO_VERS = D.CUIIO_VERS
 
WHERE
  D.PERIOADA IN (1063) AND 


  D.FORM IN (45)    
 
GROUP BY
  D.CUIIO,
  R.CAEM2,
  R.DENUMIRE,
  C.FULL_CODE,
  D.CUIIO_VERS,
  C.CODUL,
  C.DENUMIRE
 
  
ORDER BY
C.FULL_CODE