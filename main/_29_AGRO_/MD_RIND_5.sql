
SELECT 
D.RIND,
D.RIND_VERS,
D.ORDINE + 110 ORDINE
FROM CIS2.MD_RIND D

INNER JOIN 
(
SELECT 
RIND,
MAX(RIND_VERS) RIND_VERS,
MAX(ORDINE) ORDINE 
FROM CIS2.MD_RIND
   WHERE
    FORM = 45
    AND 
    capitol IN (401)
    GROUP BY
    RIND 
   -- ORDINE
    ) DD ON D.RIND=DD.RIND AND D.RIND_VERS=DD.RIND_VERS AND D.ORDINE = DD.ORDINE



WHERE 
D.STATUT = '1'
 AND    
 (D.RIND BETWEEN '2100' AND  '2302') 

ORDER BY 
D.ORDINE