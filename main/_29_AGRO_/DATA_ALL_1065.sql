
SELECT
    D.CUIIO,
    NULLIF(SUM(CASE WHEN D.CAPITOL = 399 AND D.RIND = '1110' THEN D.COL1 ELSE 0 END), 0) AS R_1110_C1,
    NULLIF(SUM(CASE WHEN D.CAPITOL = 399 AND D.RIND = '1110' THEN D.COL2 ELSE 0 END), 0) AS R_1110_C2,
    NULLIF(SUM(CASE WHEN D.CAPITOL = 399 AND D.RIND = '1110' THEN D.COL3 ELSE 0 END), 0) AS R_1110_C3,
    NULLIF(SUM(CASE WHEN D.CAPITOL = 399 AND D.RIND = '1110' THEN D.COL4 ELSE 0 END), 0) AS R_1110_C4,
    NULLIF(SUM(CASE WHEN D.CAPITOL = 399 AND D.RIND = '1110' THEN D.COL5 ELSE 0 END), 0) AS R_1110_C5
  

   

FROM CIS2.VW_DATA_ALL D
INNER JOIN CIS2.RENIM R ON R.CUIIO = D.CUIIO AND R.CUIIO_VERS = D.CUIIO_VERS
INNER JOIN CIS2.VW_CL_CUATM C ON C.CODUL = D.CUATM
WHERE 
    D.PERIOADA IN (1066)
    AND D.FORM IN (45)
    AND D.CUIIO = 27832
   
GROUP BY
    D.CUIIO,
    D.CUATM,
    C.DENUMIRE,
    C.FULL_CODE
    
    ORDER BY
    C.FULL_CODE