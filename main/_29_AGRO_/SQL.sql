D.CUIIO,
R.DENUMIRE,
CR.RIND,
--CR.ORDINE,
SUM(CASE WHEN CR.RIND LIKE '%'||D.RIND||'%'  THEN D.COL1 ELSE NULL END)  AS COL1,
