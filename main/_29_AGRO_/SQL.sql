            INNER JOIN CIS2.RENIM R ON 
            R.CUIIO = D.CUIIO AND 
            R.CUIIO_VERS = D.CUIIO_VERS
            
            CROSS JOIN (
            SELECT
D.RIND,
D.RIND_VERS,
D.ORDINE
FROM
(
SELECT 
D.RIND,
D.RIND_VERS,
D.ORDINE
FROM CIS2.MD_RIND D
INNER JOIN 
(
SELECT 
RIND,
MAX(RIND_VERS) RIND_VERS,
MAX(ORDINE) ORDINE 
FROM CIS2.MD_RIND
   WHERE
    FORM = 45
    AND 
    capitol IN (399)
    GROUP BY
    RIND 
    
    ) 
      DD ON D.RIND=DD.RIND AND D.RIND_VERS=DD.RIND_VERS AND D.ORDINE = DD.ORDINE



