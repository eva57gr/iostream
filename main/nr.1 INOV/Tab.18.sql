--(
--  PERIOADA,
--  FORM,
--  FORM_VERS,
--  ID_MDTABLE,
--  COD_CUATM,
--  NR_SECTIE,
--  NUME_SECTIE,
--  NR_SECTIE1,
--  NUME_SECTIE1,
--  NR_SECTIE2,
--  NUME_SECTIE2,
--  NR_ROW,
--  ORDINE,
--  DECIMAL_POS,
--  NUME_ROW,  
--  COL1,COL2, COL3
--)
SELECT
  :pPERIOADA AS PERIOADA,
  :pFORM AS FORM,
  :pFORM_VERS AS FORM_VERS,
  :pID_MDTABLE AS ID_MDTABLE,
  :pCOD_CUATM AS COD_CUATM,
         
  '0' AS NR_SECTIE,
  '0' AS NUME_SECTIE,
  '0' AS NR_SECTIE1,
  '0' AS NUME_SECTIE1,
  '0' AS NR_SECTIE2,
  '0' AS NUME_SECTIE2,
   COL1||'~'||ORDINE AS NR_ROW,
   ORDINE AS ORDINE,
  '000' AS DECIMAL_POS,
  DENUMIRE AS NUME_ROW,  
  COL2,
  COL3,
  COL4
   
FROM
(
SELECT
  TR.DENUMIRE,
  TR.ORDINE,
  COUNT(DISTINCT
    CASE 
      WHEN
        TR.CONDITIE IN ('BETWEEN') AND TR.CAEM2 LIKE '%'|| SUBSTR(D.CAEM2,0,1) ||'%' AND  D.PERS_IT BETWEEN (SUBSTR(TR.RESULT,0,INSTR(RESULT,'-')-1)) AND (SUBSTR(TR.RESULT,INSTR(TR.RESULT,'-')+1,LENGTH(TR.RESULT))) AND
        D.COL1 > 0
      THEN D.CUIIO
    END) AS COL1,
  
  COUNT(DISTINCT 
    CASE 
      WHEN
        TR.CONDITIE IN ('BETWEEN') AND TR.CAEM2 LIKE '%'|| SUBSTR(D.CAEM2,0,1) ||'%' AND D.PERS_IT BETWEEN (SUBSTR(TR.RESULT,0,INSTR(RESULT,'-')-1)) AND (SUBSTR(TR.RESULT,INSTR(TR.RESULT,'-')+1,LENGTH(TR.RESULT))) AND
        D.COL2_1 > 0 AND D.COL2_2 >0
      THEN D.CUIIO
    END) AS COL2,
  
  COUNT(DISTINCT 
    CASE 
      WHEN
        TR.CONDITIE IN ('BETWEEN') AND TR.CAEM2 LIKE '%'|| SUBSTR(D.CAEM2,0,1) ||'%' AND D.PERS_IT BETWEEN (SUBSTR(TR.RESULT,0,INSTR(RESULT,'-')-1)) AND (SUBSTR(TR.RESULT,INSTR(TR.RESULT,'-')+1,LENGTH(TR.RESULT))) AND
        (D.COL3_1 > 0 AND D.COL3_2 = 0)
      THEN D.CUIIO
    END) AS COL3,
  
  COUNT(DISTINCT 
    CASE 
      WHEN
        TR.CONDITIE IN ('BETWEEN') AND TR.CAEM2 LIKE '%'|| SUBSTR(D.CAEM2,0,1) ||'%' AND D.PERS_IT BETWEEN (SUBSTR(TR.RESULT,0,INSTR(RESULT,'-')-1)) AND (SUBSTR(TR.RESULT,INSTR(TR.RESULT,'-')+1,LENGTH(TR.RESULT))) AND
        (D.COL4_1 = 0 AND D.COL4_2 > 0)
      THEN D.CUIIO
    END) AS COL4
FROM 
  (
    SELECT DISTINCT
      D.CUIIO,
      D.CAEM2,
      BS.PERS_IT,
      COUNT(DISTINCT CASE WHEN D.CAPITOL||'.'||D.RIND IN ('1041.2.1.1','1041.2.1.2', '1042.3.1.1','1042.3.1.2','1042.3.1.3','1046.7.1.1','1046.7.1.2','1046.7.1.3','1047.8.1.1','1047.8.1.2','1047.8.1.3','1047.8.1.4') AND D.COL1=1 THEN 1 END) AS COL1,
      COUNT(DISTINCT CASE WHEN D.CAPITOL||'.'||D.RIND IN ('1046.7.1.1','1046.7.1.2','1046.7.1.3','1047.8.1.1','1047.8.1.2','1047.8.1.3','1047.8.1.4') AND D.COL1=1 THEN 1 END) AS COL2_1,
      COUNT(DISTINCT CASE WHEN D.CAPITOL||'.'||D.RIND IN ('1041.2.1.1','1041.2.1.2','1042.3.1.1','1042.3.1.2','1042.3.1.3') AND D.COL1=1 THEN 1 END) AS COL2_2,
      COUNT(DISTINCT CASE WHEN D.CAPITOL||'.'||D.RIND IN ('1041.2.1.1','1041.2.1.2','1042.3.1.1','1042.3.1.2','1042.3.1.3') AND D.COL1=1 THEN 1 END) AS COL3_1,
      COUNT(DISTINCT CASE WHEN D.CAPITOL||'.'||D.RIND IN ('1046.7.1.1','1046.7.1.2','1046.7.1.3','1047.8.1.1','1047.8.1.2','1047.8.1.3','1047.8.1.4') AND D.COL1=1 THEN 1 END) AS COL3_2,
      COUNT(DISTINCT CASE WHEN D.CAPITOL||'.'||D.RIND IN ('1041.2.1.1','1041.2.1.2','1042.3.1.1','1042.3.1.2','1042.3.1.3') AND D.COL1=1 THEN 1 END) AS COL4_1,
      COUNT(DISTINCT CASE WHEN D.CAPITOL||'.'||D.RIND IN ('1046.7.1.1','1046.7.1.2','1046.7.1.3','1047.8.1.1','1047.8.1.2','1047.8.1.3','1047.8.1.4') AND D.COL1=1 THEN 1 END) AS COL4_2
    FROM
      CIS2.VW_DATA_ALL D  
      INNER JOIN
      CIS2.X_BAZA_SONDAJ BS ON D.CUIIO=BS.CUIIO AND D.ANUL=BS.ANUL
    WHERE
      D.PERIOADA IN (:pPERIOADA) AND 
      D.FORM_VERS = :pFORM_VERS     AND   
      D.FORM= :pFORM     AND    
      (:pID_MDTABLE=:pID_MDTABLE) AND
      D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%'  AND 
      D.FORM IN (48)
    GROUP BY
      D.CUIIO,
      D.CAEM2,
      BS.PERS_IT
  ) D
CROSS JOIN
 (
SELECT 'Total' AS DENUMIRE, 1 AS ORDINE, 'BETWEEN' AS CONDITIE, 'B+C+D+E+G46+H+J+K+M71+M72+M73' AS CAEM2, '0-99999999999' AS RESULT FROM DUAL UNION 
SELECT '10-49 salariati' AS DENUMIRE, 2 AS ORDINE, 'BETWEEN' AS CONDITIE, 'B+C+D+E+G46+H+J+K+M71+M72+M73' AS CAEM2, '0-49' AS RESULT FROM DUAL UNION 
SELECT '50-249 salariati' AS DENUMIRE, 3 AS ORDINE, 'BETWEEN' AS CONDITIE, 'B+C+D+E+G46+H+J+K+M71+M72+M73' AS CAEM2, '50-249' AS RESULT FROM DUAL UNION 
SELECT '250 si peste salariati' AS DENUMIRE, 4 AS ORDINE, 'BETWEEN' AS CONDITIE, 'B+C+D+E+G46+H+J+K+M71+M72+M73' AS CAEM2, '250-99999999999' AS RESULT FROM DUAL UNION
SELECT 'Industrie - total' AS DENUMIRE, 5 AS ORDINE, 'BETWEEN' AS CONDITIE, 'B+C+D+E' AS CAEM2, '0-99999999999' AS RESULT FROM DUAL UNION 
SELECT '10-49 salariati' AS DENUMIRE, 6 AS ORDINE, 'BETWEEN' AS CONDITIE, 'B+C+D+E' AS CAEM2, '0-49' AS RESULT FROM DUAL UNION 
SELECT '50-249 salariati' AS DENUMIRE, 7 AS ORDINE, 'BETWEEN' AS CONDITIE, 'B+C+D+E' AS CAEM2, '50-249' AS RESULT FROM DUAL UNION 
SELECT '250 si peste salariati' AS DENUMIRE, 8 AS ORDINE, 'BETWEEN' AS CONDITIE, 'B+C+D+E' AS CAEM2, '250-99999999999' AS RESULT FROM DUAL UNION
SELECT 'Servicii - total' AS DENUMIRE, 9 AS ORDINE, 'BETWEEN' AS CONDITIE, 'G46+H+J+K+M71+M72+M73' AS CAEM2, '0-99999999999' AS RESULT FROM DUAL UNION 
SELECT '10-49 salariati' AS DENUMIRE, 10 AS ORDINE, 'BETWEEN' AS CONDITIE, 'G46+H+J+K+M71+M72+M73' AS CAEM2, '0-49' AS RESULT FROM DUAL UNION 
SELECT '50-249 salariati' AS DENUMIRE, 11 AS ORDINE, 'BETWEEN' AS CONDITIE, 'G46+H+J+K+M71+M72+M73' AS CAEM2, '50-249' AS RESULT FROM DUAL UNION 
SELECT '250 si peste salariati' AS DENUMIRE, 12 AS ORDINE, 'BETWEEN' AS CONDITIE, 'G46+H+J+K+M71+M72+M73' AS CAEM2, '250-99999999999' AS RESULT FROM DUAL
) TR
GROUP BY
  TR.DENUMIRE,
  TR.ORDINE)