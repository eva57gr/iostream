INSERT INTO TABLE_OUT 
(
  PERIOADA,
  FORM,
  FORM_VERS,
  ID_MDTABLE,
  COD_CUATM,
  NR_SECTIE,
  NUME_SECTIE,
  NR_SECTIE1,
  NUME_SECTIE1,
  NR_SECTIE2,
  NUME_SECTIE2,
  NR_ROW,
  ORDINE,
  DECIMAL_POS,
  NUME_ROW,  
  COL1,COL2, COL3, COL4, COL5, COL6, COL7, COL8, COL9
)
SELECT
  :pPERIOADA AS PERIOADA,
  :pFORM AS FORM,
  :pFORM_VERS AS FORM_VERS,
  :pID_MDTABLE AS ID_MDTABLE,
  :pCOD_CUATM AS COD_CUATM,
         
  '0' AS  NR_SECTIE,
  '0' AS  NUME_SECTIE,
  '0' AS NR_SECTIE1,
  '0' AS NUME_SECTIE1,
  '0' as NR_SECTIE2,
  '0' as NUME_SECTIE2,
  CS_CUATM||'~'||ROWNUM NR_ROW,
  ORDINE,
  '000000000' AS DECIMAL_POS,
  NUME_ROW,
  COL1,
  COL2,
  COL3,
  COL4,
  COL5,
  COL6,
  COL7,
  COL8,
  COL9
  FROM

(
--Next
SELECT 
TO_NUMBER(CR.NR_ROW) AS ORDINE,
CR.CS_CUATM,
CR.NUME_ROW AS NUME_ROW,



SUM(CASE WHEN  C.FULL_CODE LIKE '%'||CR.CS_CUATM||'%'  THEN L.COL1 ELSE NULL END ) AS COL1,
SUM(CASE WHEN  C.FULL_CODE LIKE '%'||CR.CS_CUATM||'%'  THEN L.COL2 ELSE NULL END ) AS COL2,
SUM(CASE WHEN  C.FULL_CODE LIKE '%'||CR.CS_CUATM||'%'  THEN L.COL3 ELSE NULL END ) AS COL3,
SUM(CASE WHEN  C.FULL_CODE LIKE '%'||CR.CS_CUATM||'%'  THEN L.COL4 ELSE NULL END ) AS COL4,
SUM(CASE WHEN  C.FULL_CODE LIKE '%'||CR.CS_CUATM||'%'  THEN L.COL5 ELSE NULL END ) AS COL5,
SUM(CASE WHEN  C.FULL_CODE LIKE '%'||CR.CS_CUATM||'%'  THEN L.COL6 ELSE NULL END ) AS COL6,
SUM(CASE WHEN  C.FULL_CODE LIKE '%'||CR.CS_CUATM||'%'  THEN L.COL7 ELSE NULL END ) AS COL7,
SUM(CASE WHEN  C.FULL_CODE LIKE '%'||CR.CS_CUATM||'%'  THEN L.COL8 ELSE NULL END ) AS COL8,
SUM(CASE WHEN  C.FULL_CODE LIKE '%'||CR.CS_CUATM||'%'  THEN L.COL9 ELSE NULL END ) AS COL9


FROM 

(

SELECT 
 C.CODUL,
 C.FULL_CODE,
 COUNT (DISTINCT CASE WHEN FC.FORM  = :pFORM THEN  FC.CUIIO END)  AS   COL1,
 NULL AS COL2,
 NULL AS COL3,
 NULL AS COL4,
 NULL AS COL5,
 NULL AS COL6,
 NULL AS COL7,
 NULL AS COL8,
 NULL AS COL9
 FROM 

(
SELECT     

           R.CUIIO,
           R. CUIIO_VERS,
           FC.FORM,
          
           R.CUATM
           
      FROM (
      
  SELECT FC.CUIIO,
                   FC.CUIIO_VERS,
                   FC.FORM,
                   FC.FORM_VERS,
                   FC.STATUT
              FROM CIS2.FORM_CUIIO  FC
                         
              
                   INNER JOIN (  SELECT CUIIO, MAX (CUIIO_VERS) CUIIO_VERS
                                   FROM CIS2.FORM_CUIIO
                                  WHERE FORM IN (:pFORM) AND CUIIO_VERS <= :pPERIOADA  
                               GROUP BY CUIIO) BB
                       ON (    BB.CUIIO = FC.CUIIO
                           AND BB.CUIIO_VERS = FC.CUIIO_VERS)
             WHERE FC.FORM IN (:pFORM) AND FC.STATUT <> '3'
                        
             
             
             ) FC
           INNER JOIN CIS2.RENIM R
               ON (R.CUIIO = FC.CUIIO AND R.CUIIO_VERS = FC.CUIIO_VERS)
               
               ) FC 
          
               
                 INNER JOIN CIS2.VW_CL_CUATM C   ON C.CODUL = FC.CUATM
              
               GROUP BY 
              C.CODUL,
              C.FULL_CODE
              
              
              UNION 
              
              
SELECT
 C.CODUL,
 C.FULL_CODE,
 NULL AS COL1,
COUNT (DISTINCT CASE WHEN D.RIND IN ('01') AND NVAL(D.COL1) = 1 THEN  D.CUIIO  END ) AS COL2,
COUNT (DISTINCT CASE WHEN D.RIND IN ('02') AND NVAL(D.COL1) = 1 THEN  D.CUIIO  END ) AS COL3,
COUNT (DISTINCT CASE WHEN D.RIND IN ('03') AND NVAL(D.COL1) = 1 THEN  D.CUIIO  END ) AS COL4,
COUNT (DISTINCT CASE WHEN D.RIND IN ('04') AND NVAL(D.COL1) = 1 THEN  D.CUIIO  END ) AS COL5,
COUNT (DISTINCT CASE WHEN D.RIND IN ('05') AND NVAL(D.COL1) = 1 THEN  D.CUIIO  END ) AS COL6,
COUNT (DISTINCT CASE WHEN D.RIND IN ('06') AND NVAL(D.COL1) = 1 THEN  D.CUIIO  END ) AS COL7,
COUNT (DISTINCT CASE WHEN D.RIND IN ('07') AND NVAL(D.COL1) = 1 THEN  D.CUIIO  END ) AS COL8,
COUNT (DISTINCT CASE WHEN D.RIND IN ('ER') AND NVAL(D.COL1) = 1 THEN  D.CUIIO  END ) AS COL9

FROM
 CIS2.VW_DATA_ALL D
   INNER JOIN CIS2.VW_CL_CUATM C   ON C.CODUL = D.CUATM
 
        

WHERE
  D.PERIOADA IN (:pPERIOADA) AND 
  D.FORM_VERS = :pFORM_VERS     AND   
  D.FORM= :pFORM     AND    
  D.CUATM_FULL LIKE '%'||'0000000'||';%'  AND 
  D.FORM IN (48)
  AND D.CAPITOL IN (100)

--AND D.CUIIO = 400053

GROUP BY
 C.CODUL,
 C.FULL_CODE
  ) L
     
     INNER JOIN CIS2.VW_CL_CUATM C   ON C.CODUL = L.CODUL
     CROSS JOIN (
     
    
SELECT
      DECODE(CS.CODUL, 
      '0000000','01','0100000','02','1111111','03','0300000','04',
      '1400000','05','3400000','06','3600000','07','4100000','08','4300000','09',
      '4500000','10','4800000','11','6200000','12','7100000','13','7400000','14',
      '7800000','15','2222222','16','1000000','17','2500000','18','3100000','19',
      '3800000','20','5300000','21','5500000','22','6000000','23','6400000','24',
      '6700000','25','8000000','26','8300000','27','8900000','28','9200000','29',
      '3333333','30','1200000','31','1700000','32','2100000','33','2700000','34',
      '2900000','35','5700000','36','8500000','37','8700000','38','9600000','39',
          
               '9601000', '40',
               '9602000', '41',
               '9603000', '42'
      
      )
      AS NR_ROW,
      CS.DENUMIRE AS NUME_ROW,
      CS.CODUL AS CS_CUATM
    FROM
      CIS2.VW_CL_CUATM CS
    WHERE
      CS.CODUL 
      IN(
         '0000000','0100000','1111111','0300000','1400000','3400000','3600000','4100000','4300000','4500000',
         '4800000','6200000','7100000','7400000','7800000','2222222','1000000','2500000','3100000','3800000',
         '5300000','5500000','6000000','6400000','6700000','8000000','8300000','8900000','9200000','3333333',
         '1200000','1700000','2100000','2700000','2900000','5700000','8500000','8700000','9600000',
              
               '9601000', 
               '9602000', 
               '9603000' 
         
        )
      
     ) CR 
     
     WHERE 
     1=1
    
     group by 
    CR.NUME_ROW, 
    TO_NUMBER(CR.NR_ROW),
    CR.CS_CUATM 
      
      ORDER BY
      CR.NR_ROW
      
      )