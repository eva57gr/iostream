CREATE OR REPLACE FORCE VIEW USER_BANCU.VW_DATA_ALL_1061
(CUIIO
)

AS
SELECT

A.CUIIO
 
FROM
(SELECT
  D.ANUL,
  D.CUIIO,
  D.CUATM,
  D.CAEM2,
  D.CAPITOL,
  D.RIND,
  D.COL1, 
  D.COL2
FROM 
  CIS2.VW_DATA_ALL D
WHERE
  D.PERIOADA IN (1061) AND
  D.FORM IN (74) AND 
  D.CAPITOL IN (1197) AND
  D.RIND IN ('1.1','1.2','1.3')
) A

  
  WHERE 
  A.CAEM2 NOT LIKE 'A%' 
  
  GROUP BY
  A.CUIIO
  HAVING 
  COUNT(DISTINCT CASE WHEN A.RIND IN ('1.2')  THEN A.CUIIO END) > 0 