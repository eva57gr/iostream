SELECT
DISTINCT
--F.DEN_SHORT,
D.CUIIO,
D.CUATM,
R.DENUMIRE,
MAX(D.PACHET) AS PACHET,
MAX(PR.DATA_REG) AS DATA_PRIMIRII,
--MAX(C.DATA_REG) AS DATA_PROCESARII,
PR.CAPITOL_DEN,
PR.CAPITOL,
PR.RIND,
PR.COL1 AS COL1,
PR.COL2 AS COL2,
PR.COL3 AS COL3,
PR.COL4 AS COL4,
PR.COL5 AS COL5,
PR.COL6 AS COL6,
PR.COL7 AS COL7,
PR.COL8 AS COL8,
PR.COL9  AS COL9,
PR.COL10  AS COL10,
PR.COL11 AS COL11

FROM
CIS2.VW_DATA_ALL_FR D --M1, CONS.TS, 5CI,65 AUTO, 2 INVEST
INNER JOIN CIS2.RENIM R ON R.CUIIO = D.CUIIO AND R.CUIIO_VERS = D.CUIIO_VERS
--INNER JOIN CIS2.MD_FORM F ON (D.FORM=F.FORM AND D.FORM_VERS=F.FORM_VERS)
INNER JOIN USER_EREPORTING.VW_DATA_ALL_FR_PRIMIT PR ON PR.CUIIO = D.CUIIO AND PR.CUIIO_VERS = D.CUIIO_VERS AND D.PERIOADA=PR.PERIOADA AND D.FORM=PR.FORM AND PR.ID_SCHEMA IN (2)
INNER JOIN CIS2.CONTROL C ON (D.PERIOADA=C.PERIOADA AND D.CUIIO=C.CUIIO AND D.CUIIO_VERS=C.CUIIO_VERS)
WHERE
D.PERIOADA = :pPERIOADA_LUNARA AND
D.CUATM LIKE '01%' AND
D.FORM IN (57)
AND D.CAPITOL NOT IN (1091)

--AND D.CUIIO = 65063
GROUP BY
--F.DEN_SHORT,
D.CUIIO,
D.CUATM,
R.DENUMIRE,
PR.CAPITOL_DEN,
PR.CAPITOL,
PR.RIND,
PR.COL1,
PR.COL2,
PR.COL3, 
PR.COL4,
PR.COL5, 
PR.COL6,
PR.COL7, 
PR.COL8, 
PR.COL9, 
PR.COL10,
PR.COL11






ORDER BY

DEN_SHORT,
CUIIO,
CAPITOL,
RIND