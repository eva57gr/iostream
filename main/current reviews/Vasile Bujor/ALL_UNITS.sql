SELECT
  A.FORM,
  A.FORM_DEN,
  CC.NR_ROW,
  CC.NUME_ROW,
  A.NUM AS TRIMESTRU,
  COUNT(DISTINCT CASE WHEN C.FULL_CODE LIKE '%'||CC.CS_CUATM||'%' THEN A.CUIIO END) AS TOTAL,
  COUNT(CASE WHEN C.FULL_CODE LIKE '%'||CC.CS_CUATM||'%' THEN B.CUIIO END) AS ER_TOTAL,
  ROUND((COUNT(CASE WHEN C.FULL_CODE LIKE '%'||CC.CS_CUATM||'%' THEN B.CUIIO END)*100)/NOZERO(COUNT(CASE WHEN C.FULL_CODE LIKE '%'||CC.CS_CUATM||'%' THEN A.CUIIO END)),1)||' %' AS PERCENT
FROM
(
SELECT DISTINCT
              D.FORM,
              D.FORM_DEN,
              D.CUIIO,
              D.CUIIO_VERS,
              D.CUATM,
              D.NUM
            FROM
              CIS2.VW_DATA_ALL D
            WHERE
              D.PERIOADA BETWEEN :pPERIOADA_TRIMESTRIALA-4 AND :pPERIOADA_TRIMESTRIALA
              AND D.FORM IN (18)
)A
LEFT JOIN 
(
SELECT DISTINCT
  ED.FORM,
  ED.CUIIO,
  ED.CUIIO_VERS
FROM
  USER_EREPORTING.VW_DATA_ALL_PRIMIT ED
WHERE
  ED.FORM IN (18) AND
  ED.ID_SCHEMA IN (2) AND
  ED.PERIOADA IN (:pPERIOADA_TRIMESTRIALA)
) B ON (A.CUIIO=B.CUIIO AND A.CUIIO_VERS=B.CUIIO_VERS AND A.FORM=B.FORM)
  
INNER JOIN CIS2.VW_CL_CUATM C ON(A.CUATM = C.CODUL)
  CROSS JOIN 
  (
    SELECT
      
      DECODE(CS.CODUL, 
      '0000000','01','0100000','02','1111111','03','0300000','04',
      '1400000','05','3400000','06','3600000','07','4100000','08','4300000','09',
      '4500000','10','4800000','11','6200000','12','7100000','13','7400000','14',
      '7800000','15','2222222','16','1000000','17','2500000','18','3100000','19',
      '3800000','20','5300000','21','5500000','22','6000000','23','6400000','24',
      '6700000','25','8000000','26','8300000','27','8900000','28','9200000','29',
      '3333333','30','1200000','31','1700000','32','2100000','33','2700000','34',
      '2900000','35','5700000','36','8500000','37','8700000','38',
      '9600000','39')
      AS NR_ROW,
      CS.DENUMIRE AS NUME_ROW,
      CS.CODUL AS CS_CUATM

    FROM
      CIS2.VW_CL_CUATM CS

    WHERE
      CS.CODUL 
      IN(
         '0000000','0100000','1111111','0300000','1400000','3400000','3600000','4100000','4300000','4500000',
         '4800000','6200000','7100000','7400000','7800000','2222222','1000000','2500000','3100000','3800000',
         '5300000','5500000','6000000','6400000','6700000','8000000','8300000','8900000','9200000','3333333',
         '1200000','1700000','2100000','2700000','2900000','5700000','8500000','8700000',
         '9600000'
        )
  
  ) CC
GROUP BY
  A.FORM,
  A.FORM_DEN,
  CC.NR_ROW,
  CC.NUME_ROW,
  A.NUM
  
  
  HAVING 
  CC.NUME_ROW IN ('TOTAL')
ORDER BY
  A.FORM,
  CC.NR_ROW,
  A.NUM