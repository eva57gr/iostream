SELECT
  :pPERIOADA AS PERIOADA,
  :pFORM AS FORM,
  :pFORM_VERS AS FORM_VERS,
  :pID_MDTABLE AS ID_MDTABLE,
  :pCOD_CUATM AS COD_CUATM,
         
  ORDINE_CFOJ AS NR_SECTIE,
  NNAME_CFOJ||'('||CODE_CFJO||')' AS NUME_SECTIE,
  '0' AS NR_SECTIE1,
  '0' AS NUME_SECTIE1,
  '0' AS NR_SECTIE2,
  '0' AS NUME_SECTIE2,
  TRIM(TO_CHAR(ROUND(COL1,0),'99999999999999999999999990.9'))||'~'||NR_ROW AS NR_ROW,
  ORDINE,
  '000000' AS DECIMAL_POS,
  NUME_ROW,
  ROUND(COL2,0) AS COL1,
  ROUND((CIS2.NVAL(COL2)/CIS2.NOZERO(COL1))*100,1) AS COL2,
  ROUND(COL4,0) AS COL3,
  ROUND(COL5,0) AS COL4,
  ROUND((CIS2.NVAL(COL5)/CIS2.NOZERO(COL4))*100,1) AS COL5
FROM
(
SELECT 
  R.NR_ROW,
  R.NUME_ROW,
  TO_NUMBER(R.NR_ROW) AS ORDINE,
  CF.ORDINE AS ORDINE_CFOJ,
--  D.CFOJ,
  CF.NR_ROW AS CODE_CFJO,
  CF.NUME_ROW AS NNAME_CFOJ,
--  ((cap.II rd.140 col.1 )*100000 /cap.II rd.160 col.1) AP
  CIS2.NVAL  (SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-4) AND D.PERIOADA < 1060 AND D.RIND IN ('140') AND D.CAPITOL IN (380) AND CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' AND CF.NR_ROW LIKE '%'|| D.CFOJ ||'%' THEN CIS2.NVAL(D.COL1) END))*100000/
  CIS2.NOZERO(SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-4) AND D.PERIOADA < 1060 AND D.RIND IN ('160') AND D.CAPITOL IN (380) AND CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' AND CF.NR_ROW LIKE '%'|| D.CFOJ ||'%' THEN CIS2.NVAL(D.COL1) END)) AS COL1,
--  ((tab.1.1 rd. 2 col.1 )*100000 / tab.1.1 rd.3 col.1) AC


  CIS2.NVAL  (SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.PERIOADA >=1060 AND D.RIND IN ('2') AND D.CAPITOL IN (379) AND CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' AND CF.NR_ROW LIKE '%'|| D.CFOJ ||'%' THEN CIS2.NVAL(D.COL1) END))*100000/
  CIS2.NOZERO(SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.PERIOADA >=1060 AND D.RIND IN ('3') AND D.CAPITOL IN (379) AND CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' AND CF.NR_ROW LIKE '%'|| D.CFOJ ||'%' THEN CIS2.NVAL(D.COL1) END)) 
  
  
  
  
  AS COL2,
 --  ((cap.II rd.140 col.2 )*100000 /cap.II rd.160 col.2) AP
  CIS2.NVAL  (SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-4) AND D.PERIOADA < 1060 AND D.RIND IN ('140') AND D.CAPITOL IN (380) AND CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' AND CF.NR_ROW LIKE '%'|| D.CFOJ ||'%' THEN CIS2.NVAL(D.COL2) END))*100000/
  CIS2.NOZERO(SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-4) AND D.PERIOADA < 1060 AND D.RIND IN ('160') AND D.CAPITOL IN (380) AND CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' AND CF.NR_ROW LIKE '%'|| D.CFOJ ||'%' THEN CIS2.NVAL(D.COL2) END)) AS COL4,
-- ((tab.1.1 rd. 2 col.3)*100000 / tab.1.1 rd.3 col.3) AC
  CIS2.NVAL  (SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.PERIOADA >=1060 AND D.RIND IN ('2') AND D.CAPITOL IN (379) AND CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' AND CF.NR_ROW LIKE '%'|| D.CFOJ ||'%' THEN CIS2.NVAL(D.COL3) END))*100000/
  CIS2.NOZERO(SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.PERIOADA >=1060 AND D.RIND IN ('3') AND D.CAPITOL IN (379) AND CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' AND CF.NR_ROW LIKE '%'|| D.CFOJ ||'%' THEN CIS2.NVAL(D.COL3) END)) AS COL5

 
FROM 
  CIS2.VW_DATA_ALL D
  INNER JOIN CIS2.RENIM RE ON (D.CUIIO=RE.CUIIO AND D.CUIIO_VERS=RE.CUIIO_VERS)
  CROSS JOIN
  (
    SELECT '420+450+684+685+686' AS NR_ROW, 'Gospodariile taranesti (de fermier), care au la balanta animale' AS NUME_ROW, 5 AS ORDINE, '33' AS FORM FROM DUAL UNION
    SELECT '880' AS NR_ROW, 'Gospodariile populatiei' AS NUME_ROW, 8 AS ORDINE, '29' AS FORM FROM DUAL UNION
    SELECT '590+620+880' AS NR_ROW, 'Gospodariile auxiliare' AS NUME_ROW, 7 AS ORDINE, '33' AS FORM FROM DUAL UNION
    SELECT '500+510+530+540+541+550+690+700+890+970' AS NR_ROW, 'Intreprinderile agricole' AS NUME_ROW, 6 AS ORDINE, '33' AS FORM FROM DUAL UNION
    SELECT '500+510+530+540+541+550+690+700+890+970+420+450+684+685+686' AS NR_ROW, 'Intreprinderile agricole si gospodariile taranesti (de fermier), care au balanta animale' AS NUME_ROW, 4 AS ORDINE, '33' AS FORM FROM DUAL UNION
    SELECT '500+510+530+540+541+550+690+700+890+970+420+450+880+590+620+684+685+686' AS NR_ROW, 'Gospodariile de toate categoriile' AS NUME_ROW, 1 AS ORDINE, '29,33' AS FORM FROM DUAL UNION
    SELECT '500+510+530+540+541+550+690+700+890+970+590+620+880' AS NR_ROW, 'Intreprinderile agricole(gosp.auxiliare)' AS NUME_ROW, 3 AS ORDINE, '33' AS FORM FROM DUAL UNION
    SELECT '500+510+530+540+541+550+690+700+890+970+420+450+880+590+620+684+685+686' AS NR_ROW, 'Intreprinderile agricole (gosp. auxiliare) si gospodariile taranesti (de fermier), care au la balanta animale' AS NUME_ROW, 2 AS ORDINE, '33' AS FORM FROM DUAL
  ) CF
  INNER JOIN CIS2.VW_CL_CUATM CC ON(D.CUATM = CC.CODUL)
  CROSS JOIN 
  (
    SELECT
      DECODE(CS.CODUL, 
      '0000000','01','0100000','02','1111111','03','0300000','04',
      '1400000','05','3400000','06','3600000','07','4100000','08','4300000','09',
      '4500000','10','4800000','11','6200000','12','7100000','13','7400000','14',
      '7800000','15','2222222','16','1000000','17','2500000','18','3100000','19',
      '3800000','20','5300000','21','5500000','22','6000000','23','6400000','24',
      '6700000','25','8000000','26','8300000','27','8900000','28','9200000','29',
      '3333333','30','1200000','31','1700000','32','2100000','33','2700000','34',
      '2900000','35','5700000','36','8500000','37','8700000','38','9600000','39')
      AS NR_ROW,
      CS.DENUMIRE AS NUME_ROW,
      CS.CODUL AS CS_CUATM
    FROM
      CIS2.VW_CL_CUATM CS
    WHERE
      CS.CODUL 
      IN(
         '0000000','0100000','1111111','0300000','1400000','3400000','3600000','4100000','4300000','4500000',
         '4800000','6200000','7100000','7400000','7800000','2222222','1000000','2500000','3100000','3800000',
         '5300000','5500000','6000000','6400000','6700000','8000000','8300000','8900000','9200000','3333333',
         '1200000','1700000','2100000','2700000','2900000','5700000','8500000','8700000','9600000'
        )
  ) R 
WHERE
  D.PERIOADA IN (:pPERIOADA,:pPERIOADA-4) AND 
  D.FORM_VERS = :pFORM_VERS     AND    
  (:pID_MDTABLE=:pID_MDTABLE) AND
  D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%' AND
  D.FORM IN (33)                 
 -- D.CAPITOL IN (380) AND
  --D.RIND IN ('140','160','200')  
  AND D.CFOJ IS NOT NULL
  
GROUP BY 
  R.NR_ROW,
  R.NUME_ROW,
  CF.NR_ROW,
  CF.NUME_ROW,
  CF.ORDINE