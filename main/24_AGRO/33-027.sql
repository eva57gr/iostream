SELECT DISTINCT 


    'Raportul nu satisface conditia validarii'

  
  AS REZULTAT

FROM
  CIS2.VW_DATA_ALL_TEMP D
WHERE
  (D.PERIOADA=:PERIOADA  ) AND
  (D.CUIIO=:CUIIO        ) AND
  (D.CUIIO_VERS=:CUIIO_VERS     OR :CUIIO_VERS = -1) AND
  (D.FORM = :FORM       ) AND
  (D.FORM_VERS=:FORM_VERS ) AND
  (:CAPITOL=:CAPITOL             OR :CAPITOL <> :CAPITOL) AND
  (:CAPITOL_VERS=:CAPITOL_VERS   OR :CAPITOL_VERS <> :CAPITOL_VERS) AND
  (D.ID_MD=:ID_MD                OR :ID_MD = -1) AND
  
  D.FORM IN (33) 
  
  AND
  D.CAPITOL IN (381,379) 


HAVING
    CIS2.NVAL(SUM(CASE WHEN D.RIND IN( '1','5','6','10' ) AND D.CAPITOL IN (381) THEN D.COL1 ELSE 0 END))  > 0 
    
    AND 
    CIS2.NVAL(SUM(CASE WHEN D.RIND IN( '3' ) AND D.CAPITOL IN (379) THEN NVAL(D.COL1) + NVAL(D.COL3) + NVAL(D.COL4) + NVAL(D.COL6)  ELSE 0 END))  >  0 
    
