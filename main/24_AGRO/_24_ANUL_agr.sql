SELECT 
D.CUIIO,
RE.CUATM,
RE.DENUMIRE,
D.RIND,
SUM(D.COL4) AS COL4

 

FROM 
  VW_DATA_ALL D
  INNER JOIN RENIM RE ON (D.CUIIO=RE.CUIIO AND D.CUIIO_VERS=RE.CUIIO_VERS)
 
WHERE
  D.PERIOADA IN (:pPERIOADA) AND 

  D.FORM IN (27)                 AND 
  D.CAPITOL IN (371) AND
  D.RIND IN ('55')
  
  GROUP BY 
  D.CUIIO,
  RE.CUATM,
RE.DENUMIRE,
D.RIND

HAVING 

SUM(D.COL4) IS NOT NULL 