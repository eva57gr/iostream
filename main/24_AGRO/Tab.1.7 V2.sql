  NVAL(SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-1) AND D.RIND IN ('89') THEN D.COL1 END)) AS COL1,
  NVAL(SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.RIND IN ('89') THEN D.COL1 END)) AS COL2,
  
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-1) AND D.RIND IN ('89') THEN D.COL2 END) AS COL4,
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.RIND IN ('89') THEN D.COL2 END) AS COL5,
  
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-1) AND D.RIND IN ('89') THEN D.COL4 END) AS COL7,
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.RIND IN ('89') THEN D.COL4 END) AS COL8,
  
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.RIND IN ('106') THEN D.COL1 END) AS COL10,
  
  NVAL(SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-1) AND D.RIND IN ('89') THEN D.COL6 END)) AS COL11,
  NVAL(SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.RIND IN ('89') THEN D.COL6 END)) AS COL12,
  
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-1) AND D.RIND IN ('89') THEN D.COL8 END) AS COL14,
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.RIND IN ('89') THEN D.COL8 END) AS COL15,
  
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-1) AND D.RIND IN ('153') THEN NVAL(D.COL1)+NVAL(D.COL2) END) AS COL17,
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.RIND IN ('153') THEN NVAL(D.COL1)+NVAL(D.COL2) END) AS COL18,
  
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-1) AND D.RIND IN ('147') THEN D.COL1 END) AS COL20,
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.RIND IN ('147') THEN D.COL1 END) AS COL21,
  
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-1) AND D.RIND IN ('118') THEN D.COL1 END) AS COL23,
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA) AND D.RIND IN ('118') THEN D.COL1 END) AS COL24
  
FROM 
  VW_DATA_ALL D
  INNER JOIN RENIM RE ON (D.CUIIO=RE.CUIIO AND D.CUIIO_VERS=RE.CUIIO_VERS)
WHERE
  D.PERIOADA IN (:pPERIOADA,:pPERIOADA-1) AND 
  D.FORM_VERS = :pFORM_VERS     AND    
  (:pID_MDTABLE=:pID_MDTABLE) AND
  D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%' AND
  D.FORM IN (27)                 AND 
  D.CAPITOL IN (371,372,373)           
);
   END IF;
  END LOOP;
END; -- =======================================================================