




SELECT 

--    :pPERIOADA AS PERIOADA,
--    :pFORM AS FORM,
--    :pFORM_VERS AS FORM_VERS,
--    :pID_MDTABLE AS ID_MDTABLE,
--    :pCOD_CUATM AS COD_CUATM,
--    '0' AS NR_SECTIE,
--    '0' AS NUME_SECTIE,
--    '0' AS NR_SECTIE1,
--    '0' AS NUME_SECTIE1,
--    '0' AS NR_SECTIE2,
--    '0' AS NUME_SECTIE2, 

     NR_ROW||'~'||ROWNUM NR_ROW,
ORDINE,
    '1111' AS DECIMAL_POS,
NUME_ROW,
COL1,
COL2,
COL3,
COL4


FROM
(
SELECT
ORDINE,
NR_ROW,
NUME_ROW,
SUM(D.COL1) AS COL1,
SUM(D.COL2) AS COL2,
SUM(D.COL3) AS COL3,
SUM(D.COL4) AS COL4



FROM CIS2.TABLE_OUT D


WHERE 
D.ID_MDTABLE = 12100 AND 
--D.PERIOADA IN (:pPERIOADA) AND 
 (D.PERIOADA BETWEEN FLOOR(:pPERIOADA/4)*4 AND :pPERIOADA)  AND  
 D.FORM = (:pFORM)  AND 

  (D.COD_CUATM LIKE '%'||:pCOD_CUATM||'%') 

GROUP BY 
NUME_ROW,
ORDINE,
NR_ROW


ORDER BY 
ORDINE
)







 
  
  