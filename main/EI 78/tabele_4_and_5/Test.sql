SELECT 
    D.PERIOADA,
    D.CUIIO,
    SUM(D.COL4) COL3
FROM CIS2.DATA_ALL D 
   WHERE 
  (D.PERIOADA BETWEEN FLOOR(:pPERIOADA/4)*4 AND :pPERIOADA                 ) AND 
 
  (D.FORM =:pFORM) AND
 
  D.FORM IN (44) 
  
  AND D.CUIIO = 19206
 
  GROUP BY 
D.PERIOADA,
  D.CUIIO
--  CR.COL1
  
  ORDER BY
  D.CUIIO,
  D.PERIOADA
  
 