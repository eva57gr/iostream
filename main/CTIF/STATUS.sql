SELECT 
COUNT (DISTINCT CUIIO ) AS CNT
FROM 


(
SELECT 
DISTINCT CUIIO,
DATA_REG DATA_REG

FROM USER_EREPORTING.VW_DATA_ALL_PRIMIT
WHERE
1=1




GROUP BY 
CUIIO,
DATA_REG


HAVING 

DATA_REG >= TO_DATE('10/01/2023 00:00:00', 'MM/DD/YYYY HH24:MI:SS')
AND 
DATA_REG <= TO_DATE('12/31/2023 00:00:00', 'MM/DD/YYYY HH24:MI:SS')

ORDER BY 
CUIIO,
DATA_REG  DESC)