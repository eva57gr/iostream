
SELECT
DISTINCT 
D.CUIIO,
R.DENUMIRE,
D.RIND,
SUM (D.COL1) AS COL1
FROM 
  CIS2.VW_DATA_ALL D
  INNER JOIN CIS2.RENIM R ON (R.CUIIO = D.CUIIO AND R.CUIIO_VERS = D.CUIIO_VERS)
WHERE 
  (D.PERIOADA =:pPERIOADA) AND
  
  D.FORM = 40  
  AND D.CAPITOL = 1026  AND RIND IN ('19')

GROUP BY
D.CUIIO,
R.DENUMIRE,
D.RIND

   



  
