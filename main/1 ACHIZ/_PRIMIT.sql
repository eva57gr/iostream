
SELECT 
D.CAPITOL,
D.CAPITOL_VERS,
D.RIND,
D.RIND_VERS,
SUM(D.COL1) AS COL1,
SUM(D.COL2) AS COL2,
SUM(D.COL3) AS COL3,
SUM(D.COL4) AS COL4,
SUM(D.COL5) AS COL5,
SUM(D.COL6) AS COL6,
SUM(D.COL7) AS COL7

FROM USER_EREPORTING.VW_DATA_ALL_PRIMIT D

WHERE
FORM =  54
AND PERIOADA  = 2013
AND CAPITOL = 1076

AND CUIIO = 490754

GROUP BY
D.CAPITOL,
D.CAPITOL_VERS,
D.RIND,
D.RIND_VERS