SELECT *
FROM USER_BANCU.AGRO_36
WHERE CUIIO IN (
    SELECT CUIIO
    FROM USER_BANCU.AGRO_36
    GROUP BY CUIIO
    HAVING COUNT(*) > 1
);


DELETE FROM USER_BANCU.AGRO_36
WHERE ROWID IN (
    SELECT ROWID
    FROM (
        SELECT ROWID,
               ROW_NUMBER() OVER (PARTITION BY CUIIO ORDER BY ROWID) AS rn
        FROM USER_BANCU.AGRO_36
    )
    WHERE rn > 1
);


