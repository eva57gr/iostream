INSERT INTO  CIS2.MD_RIND
  (
  ID_MD,
  FORM,
  FORM_VERS,
  CAPITOL,
  CAPITOL_VERS,
  RIND,
  RIND_VERS,
  DENUMIRE,
  DECIMAL_POS,
  COL_ACTIV,
  ORDINE,
  DATA_REG,
  STATUT,
  DINAMIC  
  )


 SELECT
  ROWNUM ID_MD,
  FORM,
  FORM_VERS,
  CAPITOL,
  CAPITOL_VERS,
  RIND,
  RIND_VERS,
  DENUMIRE,
  DECIMAL_POS,
  COL_ACTIV,
  2000 + ROWNUM ORDINE,
  DATA_REG,
  STATUT,
  DINAMIC  
FROM
(
SELECT 
  NULL ID_MD,
  49 FORM,
  2000 FORM_VERS,
  1049 CAPITOL,
  2013 CAPITOL_VERS,
  TRIM(('2'||TRIM(RIND))) RIND,
  2013 RIND_VERS,
  DENUMIRE,
  '000000000000' DECIMAL_POS,
  111111111111 COL_ACTIV,
  TO_NUMBER(ORDINE) ORDINE,
  ORDINE AS ORDINE_OLD,
  SYSDATE DATA_REG,
  '1'STATUT,
  '1'DINAMIC  

FROM USER_BANCU.CIS2_MD_RIND



ORDER BY
ORDINE_OLD 
)