INSERT INTO CIS2.TABLE_OUT 
(
  PERIOADA,
  FORM,
  FORM_VERS,
  ID_MDTABLE,
  COD_CUATM,
  NR_SECTIE,
  NUME_SECTIE,
  NR_SECTIE1,
  NUME_SECTIE1,
  NR_SECTIE2,
  NUME_SECTIE2,
  NR_ROW,
  ORDINE,
  DECIMAL_POS,
  NUME_ROW,  
  COL1,COL2, COL3, COL4, COL5, COL6, COL7, COL8, COL9, COL10, COL11, COL12, COL13, COL14, COL15--, COL16
)

SELECT
  :pPERIOADA AS PERIOADA,
  :pFORM AS FORM,
  :pFORM_VERS AS FORM_VERS,
  :pID_MDTABLE AS ID_MDTABLE,
  :pCOD_CUATM AS COD_CUATM,
         
  '0' AS NR_SECTIE,
  '0' AS NUME_SECTIE,
  '0' AS NR_SECTIE1,
  '0' AS NUME_SECTIE1,
  '0' as NR_SECTIE2,
  '0' as NUME_SECTIE2,
  NR_ROW AS NR_ROW,
  ROWNUM AS ORDINE,
  '00000000000000000' AS DECIMAL_POS,
  NUME_ROW AS NUME_ROW,
  COL1,COL2, COL3, COL4, COL5, COL6, COL7, COL8, COL9, COL10, COL11, COL12, COL13, COL14, COL15--, COL16
FROM
(
SELECT 

  TO_NUMBER(ED.CODUL) AS NR_ROW, 
  ED.FULL_CODE AS ORDINE,
  ED.DENUMIRE AS NUME_ROW, 
 -- SUBSTR(D.RIND,2,10) AS NR_ROW,
 -- SUBSTR(D.RIND,2,10) AS ORDINE,
 
  
--COUNT(DISTINCT (CASE WHEN SUBSTR(D.RIND,2,10)>0 THEN RTRIM (D.CUIIO,'0') END )) AS COL1,
  
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 THEN D.COL1 END)) AS COL1,
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 THEN D.COL2 END)) AS COL2,
 
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 THEN D.COL1 END)) -
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 THEN D.COL2 END)) AS COL3,
  
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 THEN D.COL1 END)) -
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 THEN D.COL3 END)) AS COL4,
  
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 THEN D.COL3 END)) AS COL5,
  
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (1) THEN D.COL1  END)) AS COL6,
  
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (1) THEN D.COL2 END)) AS COL7,
 
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (1) THEN D.COL1 END)) -
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (1) THEN D.COL2 END)) AS COL8,
 
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (1) THEN D.COL1 END)) -
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (1) THEN D.COL3 END)) AS COL9,
  
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (1) THEN D.COL3 END)) AS COL10,
  
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (25) THEN D.COL1 END)) AS COL11,
  
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (25) THEN D.COL2 END)) AS COL12,
 
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (25) THEN D.COL1 END)) -
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (25) THEN D.COL2 END)) AS COL13,
 
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (25) THEN D.COL1 END)) -
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (25) THEN D.COL3 END)) AS COL14,
  
  CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN 1058 AND SUBSTR(D.RIND,2,10)>0 AND R.NFI IN (25) THEN D.COL3 END)) AS COL15
  
---
FROM 

   CIS2.VW_CL_SPEC_3EDU ED
  LEFT  JOIN CIS2.VW_CL_SPEC_3EDU EDD ON ( EDD.FULL_CODE LIKE  '%'||ED.CODUL||';%')
  LEFT  JOIN 
  (
    SELECT D.* FROM CIS2.VW_DATA_ALL D
    WHERE D.PERIOADA IN (:pPERIOADA)
      AND D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%'
      AND D.FORM       IN(50)
      AND D.CAPITOL    IN(1058)
      --AND D.CUIIO =400001
    
  ) D ON (TO_CHAR(SUBSTR(D.RIND,2,10),'0000000')= EDD.CODUL)  
  INNER JOIN CIS2.RENIM R ON (D.CUIIO=R.CUIIO AND D.CUIIO_VERS=R.CUIIO_VERS)
   
  WHERE
  ID_MD NOT BETWEEN 36895 AND 37083  AND
  D.RIND NOT IN ('010','020','030','040','050','060','090','100','110','120')
  
 GROUP BY 
   ED.CODUL,
   ED.FULL_CODE,
   ED.DENUMIRE
   
 ORDER BY  
   ED.FULL_CODE
   )